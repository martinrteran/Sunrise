void Write_Command(uint16_t add,uint8_t cmd){
	uint8_t dataBuff[2];
	dataBuff[0]=0x00;
	dataBuff[1]=cmd;
	HAL_I2C_Master_Transmit(&hi2c1,add, dataBuff, 2, 10);
}
void Write_Data(uint16_t add,uint8_t* data,int buffSize){
	uint8_t dt[buffSize+1];
	dt[0]=0x40;
	memcpy(dt+1,data,buffSize);
	HAL_I2C_Master_Transmit(&hi2c1, add, dt, buffSize, 100);

}
void DISP_INIT(){
	WRITE_COMMAND(0xAE);
	{// ADDRESSING MODE
	WRITE_COMMAND(0x20);
		WRITE_COMMAND(0x01);// vertical addressing mode, 00 for horizontal and 10 for page one.
	WRITE_COMMAND(0x21);//Set up the column addresses
		WRITE_COMMAND(0x00);//first column
		WRITE_COMMAND(0x7F);//last column
	WRITE_COMMAND(0x22);// Set up pages addresses
		WRITE_COMMAND(0x00);//first
		WRITE_COMMAND(0x7);//last
	}
	{// SET MULTIPLEXER SIZE (SPACE OF THE BUFFER AVAILABLE)
		WRITE_COMMAND(0xA8);
		WRITE_COMMAND(0x3F);
	}
	{// SET DISPLAY OFFSET
		WRITE_COMMAND(0xD3);
		WRITE_COMMAND(0x00);//0 offset
	}
	{ // SET START LINE
		WRITE_COMMAND(0x40);//0x40
	}
	{//SET SEGMENT REMAP (COLUMNS) AND SET COM OUTPUT SCAN DIRECTION
		WRITE_COMMAND(0xA1);//COLUMNS REMAP
		WRITE_COMMAND(0xC8);//UP DOWN
	}
	{//SET COM PINS CONFIG
		WRITE_COMMAND(0xDA);
		WRITE_COMMAND(0x12);//(RESET)
	}
	{
	//Set Contrast
		WRITE_COMMAND(0x81);
		WRITE_COMMAND(0x7F);//CONTRAST LEVEL
	}
	{//ON DISP-RAM AND NORMAL MODE
		WRITE_COMMAND(0xA4);
		WRITE_COMMAND(0xA6);// Set normal mode for RAM
	}
	{
		//SET UP CLOCK
		WRITE_COMMAND(0xD5);
		WRITE_COMMAND(0x80);
	}
	{// PRE-CHARGE PERIOD
		WRITE_COMMAND(0xD9);
		WRITE_COMMAND(0x22);
	}
	{//SET COMMON HIGH VOLTAGE DESELECT LEVEL
		WRITE_COMMAND(0xDB);
		WRITE_COMMAND(0x20);
	}
	TURN_ON_DISP();
}

